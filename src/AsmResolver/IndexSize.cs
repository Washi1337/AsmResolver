namespace AsmResolver
{
    /// <summary>
    /// Provides possible values for the size of an index.
    /// </summary>
    public enum IndexSize
    {
        /// <summary>
        /// Indicates the index is 16-bits wide.
        /// </summary>
        Short = 2,

        /// <summary>
        /// Indicates the index is 32-bits wide.
        /// </summary>
        Long = 4,
    }
}
